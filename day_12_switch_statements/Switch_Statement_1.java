package day_12_switch_statements;

public class Switch_Statement_1 {

	public static void main(String[] args) {
		
		int num = 4;
		
		switch (num) {
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
			break;
		case 3:
			System.out.println("Three");
			break;
			
		default:
			break;
		}
		
		System.out.println("At line 24"); //just to show code after switch statement
		
	
		
		
		
		
		

	}

}
